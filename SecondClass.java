class Pattern {
    public static void main(String[] args) {
        //Print the pattern using different print statements
        System.out.println("*");
        System.out.println("**");
        System.out.println("***");
        System.out.println("****");
        System.out.println();
        //Giving Line Break
        
        //Print the pattern using a single print statement
        System.out.println("*\n**");
        System.out.println();
        //Giving Line Break
        
        //Print the pattern using a single print statement Part - 2
        System.out.println("*\n**\n***\n****");
    }
}
